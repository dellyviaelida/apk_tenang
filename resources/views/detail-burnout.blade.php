        <h3 class="guide-title">Ciri-Ciri Burnout</h3>
            <p style="margin-top: 40px;">
            Burnout syndrome merupakan istilah yang digunakan untuk menggambarkan kondisi dimana seseorang merasa stres dan mengalami kelelahan secara emosional dan secara fisik dan juga dapat menyebabkan seseorang menjadi perfeksionis dan merasa pesimis. Burnout syndrome menggambarkan perasaan kegagalan dan kelesuan akibat tuntutan yang terlalu membebankan tenaga dan kemampuan seseorang sehingga terjadi level stres karena pekerjaan sudah mencapai titik puncak. Burnout tidak boleh dibiarkan berlarut-larut dan perlu diatasi dengan tepat karena dapat memengaruhi kesehatan fisik dan mental.
